package CharTable;
public class CharacterAtributs {
    String character;
    int frequency = 0;
    String binaryASCII;
    String compression;

    public String getCharacter() {
        return character;
    }
    public void setCharacter(String character) {
        this.character = character;
    }
    public int getFrequency() {
        return frequency;
    }
    public void setFrequency(int frequency) {
        this.frequency = frequency;
    }
    public String getBinaryASCII() {
        return binaryASCII;
    }
    public void setBinaryASCII(String binaryASCII) {
        this.binaryASCII = binaryASCII;
    }
    public String getCompression() {
        return compression;
    }
    public void setCompression(String compression) {
        this.compression = compression;
    }
    

}